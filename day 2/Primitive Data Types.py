# Day 2, Section 1: Understanding Primitive Data Types in Python

# Python has four main primitive data types:
# 1. Strings
# 2. Integers
# 3. Floats
# 4. Booleans

# 1. Strings
# A string is a sequence of characters enclosed in quotes.
# Example:
string_example = "Hello, World!"
print(string_example)

# 2. Integers
# An integer is a whole number without a decimal point.
# Example:
integer_example = 25
print(integer_example)

# 3. Floats
# A float is a number with a decimal point.
# Example:
float_example = 3.14
print(float_example)

# 4. Booleans
# A boolean is either True or False.
# Example:
boolean_example = True
print(boolean_example)

# You can use the `type()` function to check the data type of any variable.
print(type(string_example))  # Outputs: <class 'str'>
print(type(integer_example))  # Outputs: <class 'int'>
print(type(float_example))  # Outputs: <class 'float'>
print(type(boolean_example))  # Outputs: <class 'bool'>
